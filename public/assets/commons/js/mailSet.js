$(function () {
    $('.mailChange').mailChange({
        ex: ','
    });
});
(function ($) {
    $.fn.mailChange = function (options) {
        /*================================================================================================================
         **初期設定*********************************************************************************************************
         ================================================================================================================*/
        var defaults = {
            ex: ','
        };
        var set = $.extend(defaults, options);
        var $this = $(this);
        var _fn = {
            /*================================================================================================================
             **OBJ操作*********************************************************************************************************
             ================================================================================================================*/
            init: function () {
                var mail = $this.text().replace(set.ex,'@');
                var $mail = '<a href="mailto:' + mail + '">' + mail + '</a>';
                $this.replaceWith($mail);
            }
        }//_fn
        _fn.init();
    }//activity
})(jQuery);