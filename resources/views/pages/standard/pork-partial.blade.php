                    <th class="leftSolid">（かたロース）<br>（うで）</th>
                    <td>肩関節直上部で背線と平行に切り開き、肩甲骨上端部で背線と平行に切断して、「かたロース」と「うで」に分離する。「かたロース」の先端は「かた」切断面と平行になるように切り落として整形する。</td>
                </tr>
                <tr>
                    <th rowspan="3">ヒレ<br>ロース<br>ばら</th>
                    <td rowspan="3">恥骨の前下方において「ヒレ」を後端から最後腰椎の部分まで切り離し、後肢外側の大腿筋膜張筋（とも三角）の前縁に沿って腹直筋（ささ肉）を切り進み、仙椎と最後腰椎との結合部において背線とほぼ直角に切断して、｢ヒレ･ロース･ばら｣を分離する。次いで、「ヒレ」を分離する。次に、横隔膜の体壁付着部を除去し、胸椎、腰椎、胸骨（剣状軟骨を含む）を除骨（肩甲軟骨を含む）したのち、第5肋骨の長さ（肋軟骨は含まない）の3分の1に相当するところで背線とほぼ平行に切断して、「ロース」と「ばら」に分離する。</td>
                    <th>ヒレ</th>
                    <td>表面に付着する脂肪は全て除去し、大腰筋と小腰筋の間にある腱は、形状を損なわない程度に除去する。</td>
                </tr>
                <tr>
                    <th class="leftSolid">ロース</th>
                    <td>肋間筋に残る壁側胸膜及び胸椎跡に残る小肉を除去する。背脂肪を棘突起跡の筋肉面に沿って切り落とし、次いで、脂肪の厚さを8mm以内に除去して整形する。「もも」との切断面のふぞろいは、「かた」切断面とほぼ平行になるように切り落として整形する。</td>
                </tr>
                <tr>
                    <th class="leftSolid">ばら</th>
                    <td>白線部を10mmの幅で除去する。腹部脂肪を除去し、次いで、脂肪の厚さを8mm以内に除去して整形する。「もも」との切断面のふぞろいは、「かた」切断面とほぼ平行になるように切り落として整形する。</td>
                </tr>
                <tr>
                    <th>もも</th>
                    <td>寛骨、大腿骨、膝蓋骨、下腿骨、足根骨、仙椎及び尾椎を除骨する。</td>
                    <th>もも</th>
                    <td>寛骨内面の小肉（めがねの小肉）を取り除き、膝蓋骨及びともずねに付着する靭帯を除去する。背脂肪は筋肉面に沿って切り落とし、脂肪の厚さを8mm以内に除去し、併せてリンパ節を除去して整形する。</td>
                </tr>
