                    <th>かた</th>
                    <td>肋間筋に残る壁側胸膜（うすかわ）を除去し、胸椎、頚椎、肩甲骨跡周辺に残る軟骨及び小肉と、その他血液、リンパ節及び靱帯を除去する。背脂肪を棘突起跡の筋肉面に沿って切り落とし、脂肪の厚さを8mm以内に除去して整形する。頬部（ジョール）の内面脂肪は、リンパ節とともに筋層まで除去し、筋肉の先端で切り落として整形する。</td>
